package cwiczenia_02_03;

public class cw03 {
    public static void main(String[] args) {
        // Deklaracja zmiennej
        int liczba = 11;

        // Sprawdzenie
        if (liczba % 2 == 0) {
            System.out.println("Liczba " + liczba + " jest parzysta.");
        } else {
            System.out.println("Liczba " + liczba + " jest nieparzysta.");
        }
    }
}
