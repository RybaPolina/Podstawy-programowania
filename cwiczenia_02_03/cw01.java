package cwiczenia_02_03;

public class cw01 {
    public static void main(String[] args) {
        // Deklaracja zmiennych
        int a = 10;
        int b = 2;

        // Obliczanie
        int plus = a + b;
        int minus = a - b;
        int multiply = a * b;
        int divide = a / b;

        // Wyświetlenie wyniku
        System.out.println("Dodawanie: " + plus);
        System.out.println("Odejmowanie: " + minus);
        System.out.println("Mnożenie: " + multiply);
        System.out.println("Dzielenie: " + divide);
        
    }
}
